      <h2>What is IVF?</h2>
                <p>In Vitro Fertilization (IVF) is a revolutionary assisted reproductive technology that offers hope to couples and individuals struggling with infertility. It is a process that involves the fertilization of eggs with sperm outside the human body.</p>
                <h2>Here's how it works:</h2>
                <ul>
                    <li>Ovulation Induction</li>
                    <p>The IVF process typically begins with ovarian stimulation. Our expert team will prescribe medications to stimulate the ovaries to produce multiple eggs. We carefully monitor this process to ensure optimal egg development.</p>
                    <li>Egg Retrieval</li>
                    <p>When the eggs are ready, a minimally invasive procedure known as egg retrieval is performed. This is usually done under sedation to minimize discomfort. The eggs are retrieved using a thin needle, guided by ultrasound, and collected for further processing.</p>
                    <li>Sperm Collection </li>
                    <p>While the eggs are being retrieved, a sperm sample is collected from the male partner or a sperm donor. Our laboratory team selects the healthiest sperm to be used in the fertilization process.</p>
                    <li>Fertilization </li>
                    <p>The retrieved eggs and selected sperm are then combined in a controlled environment within our state-of-the-art laboratory. This is where fertilization takes place. Our embryologists closely monitor the process and ensure the formation of healthy embryos.</p>
                    <li>Embryo Culture </li>
                    <p>The resulting embryos are cultured and monitored for several days to determine their quality. Our team selects the most viable embryos for transfer.</p>
                    <li>Embryo Transfer </li>
                    <p>Once the embryos are ready, one or more are transferred into the uterus. This is a relatively simple and painless procedure, similar to a regular gynaecological exam. The number of embryos transferred depends on various factors, including age and individual circumstances.</p>
                    <li>Pregnancy Test</li>
                    <p>After the embryo transfer, you'll need to wait for approximately two weeks before undergoing a pregnancy test to confirm whether the IVF cycle was successful.</p>
                  
                </ul>
              
